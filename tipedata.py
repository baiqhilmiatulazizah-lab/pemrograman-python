#tipe data pada python

#tipe data integer > angka yang ga ada komanya (bilangan bulat)
datainteger = 99
print ("data : ", datainteger)
print ("bertipe:", type(datainteger))

#tipe data float > angka dengan koma (bilangan desimal)
datafloat = 2.8
print ("data : ", datafloat)
print ("bertipe:", type(datafloat))

#tipe data string > kumpulan karakter yang ditandai dengan tanda "" atau ''
datastring = "hilmi"
print ("data : ", datastring)
print ("bertipe:", type(datastring))

#tipe data boolean > biner (01) true/false
databool = True
print ("data : ", databool)
print ("bertipe:", type(databool))

