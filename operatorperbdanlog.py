#operator != (tidak sama dengan) dan == (sama dengan)
x = 2 #integer
y = '2' #string
w = 2
z = x != y #true (benar)
print(z) 
p = x == w #true (benar)
print (p)

#operator > (lebih besar dari) dan < (lebih kecil dari)

h = 28 > 27 #true (benar)
print (h) 

a = 7 < 2 #false (salah)
print (a) 

#operator >= (lebih dari sama dengan) dan <= (kurang dari sama dengan)
b = 30
c = 23
print (b >= c) #true (benar)
print (b <= c) #false (salah)

#OPERATOR AND, OR, NOT
logikaAnd1 = z & p
logikaAnd2 = h and a
logikaOr = p or a

print(logikaAnd1) #true
print(logikaAnd2) #false
print(logikaOr) #true


